package com.tinkoff.backend.database.generator

import com.tinkoff.backend.database.dto.Sex
import com.tinkoff.backend.database.dto.UserDto
import java.time.LocalDateTime
import kotlin.random.Random
import org.springframework.stereotype.Component

@Component
class UserGenerator {


    fun generateUserDto(id: Int): UserDto {
        val sex = Sex.values().random()
        return UserDto(
            id = id,
            sex = sex,
            name = getRandomName(sex),
            age = Random.nextInt(0, 100),
            email = LocalDateTime.now().toString(),
            address = (0..3).joinToString { LocalDateTime.now().toString() }
        )
    }

    fun getRandomName(sex: Sex): String = when (sex) {
        Sex.Men -> manNames.random()
        Sex.Woman -> womanName.random()
        else -> throw RuntimeException("Неизвестный пол")
    }


}

